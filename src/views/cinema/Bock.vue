<p>It is an 1892 French short animated film directed by Émile Reynaud. Painted in 1888, it was first screened on October 28th, 1892, using the Théâtre Optique process, which allowed him to project a hand-painted colored film, before the invention of cinematograph.</p>
<p><b>Around the film</b><br>
It consists of 700 individually painted images and lasts about 15 minutes.
It is one of the first animated films ever made and was the first to be screened on Reynaud’s modified praxinoscope, the Théâtre Optique, translated as “optical theatre”.
Alongside Le Clown et ses chiens - painted in 1890 - and Pauvre Pierrot - painted in 1891 - it was exhibited in October 1892 when Émile Reynaud opened his Cabinet fantastique at the Musée Grévin. The combined performance of all three films was known as Pantomimes Lumineuses, and lasted until February 1894. These were the first animated pictures publicly exhibited by means of picture bands. Reynaud gave the whole presentation himself manipulating the images.
It is considered a lost film. No copy exists, as Reynaud threw all but two of his picture bands into the Seine.</p>
