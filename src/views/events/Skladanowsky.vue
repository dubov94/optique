<p><b>Max Skladanowsky</b><br>
(April 30, 1863 – November 30, 1939) was a German inventor and early filmmaker. Along with his brother Emil, he invented the Bioscop, an early movie projector the Skladanowsky brothers used to display the first moving picture show to a paying audience on November 1, 1895, just before the December 28, 1895 public debut of the Lumière Brothers' technically superior Cinématographe in Paris. (The Lumiere brothers had previously held a private debut of the Cinematographe on March 22, 1895.)</p>
<p><b>Life</b><br>
Born in Pankow near Berlin to a glazier, Skladanowsky was apprenticed as a photographer and glass painter, which led to an interest in magic lanterns. In 1879, he began to tour Germany and Central Europe with his father Carl and elder brother Emil, giving dissolving magic lantern shows. In the early 1890s he built a film camera along with Emil, and in 1895 the brothers produced the Bioskop. The Bioskop, which was inspired by magic-lantern technology, used two loops of 54mm film, one frame being projected alternately from each. This made it possible for the Bioskop to project at 16 frames per second, a speed sufficient to create the illusion of movement.
A demonstration of the Bioskop in Pankow, Berlin in July 1895 was witnessed by the directors of the Wintergarten music hall who contracted Skladanowsky for a sum of 2500 Goldmark to present his invention as the final act in a variety performance commencing on November 1, 1895. The show was advertised as "the most interesting invention of the modern age" and played to capacity crowds for around four weeks. The show itself consisted of a number of very short films of arounds six seconds each which were rear-projected and repeated a number of times to a specially composed musical accompaniment.
Skladanowsky's invention was booked to play the Folies Bergère in Paris from January 1896, but after the Lumière Brothers unveiled their technically superior Cinématographe show in December 1895, his contract was cancelled. Skladanowsky witnessed a performance of the Cinématographe and continued to make technical improvements to his projector and camera, touring Germany, the Netherlands, and Scandinavia throughout 1896, presenting his last show in Stettin on March 30, 1897. These later shows used a more sophisticated system with a single band of film and a geneva drive mechanism, but Skladanowsky had to stop exhibiting as the authorities refused to renew his trade licence as "too many film licences were already in number of films in the early twentieth century, some directed by Eugen, his younger brother, but with little success. In his later years Skladanowsky was accused in the press of exaggerating his role in the early days of cinema, most notably by the pioneering cameraman Guido Seeber.circulation"
After this Skladanowsky returned to his former photographic activities including the production of flip books and further magic lantern shows. He also sold amateur film cameras and projectors and produced 3-D anaglyph image slides. His company Projektion für Alle also produced a number of films in the early 20th century, some directed by Eugen, his younger brother, but with little success. In his later years Skladanowsky was accused in the press of exaggerating his role in the early days of cinema, most notably by the pioneering cameraman Guido Seeber.</p>
<p><b>Legacy</b><br>
Between the years 1895 to 1905, the brothers directed at least 25 to 30 short movies.  In 1995, the German filmmaker Wim Wenders directed a drama documentary film Die Gebrüder Skladanowsky in collaboration with students of the Munich Academy for Television and Film in which Max Skladanowsky was played by Udo Kier.</p>
